import SLPTools
import numpy as np

FRAME = ((
    (0x21, 0x22, 0x23, 0x24, 0x25),
    (0x25, 0, -2, -3, 0x21),
    (0x24, 0x24, 0x24, 0x24, 0x24)
), 
(
    (0x22, 0x23, 0x21, 0x22, 0x23),
    (0x22, 0x23, 0x21, 0x22, 0x23),
    (0x22, 0x23, 0x21, 0x22, 0x23),
))

FRAME = FRAME * 25


with open("./out.slp", "wb", ) as f:
    slp = SLPTools.encodeSLP(FRAME)
    f.write(slp)
